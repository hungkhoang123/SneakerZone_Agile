    void hover(JButton btn) {
        btn.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseEntered(MouseEvent e) {
                btn.setCursor(Cursor.getPredefinedCursor(HAND_CURSOR));
                btn.setOpaque(true);
                btn.setBackground(new Color(242, 242, 242)); // Màu khi di chuột vào
                btn.setForeground(Color.blue);
            }

            @Override
            public void mouseExited(MouseEvent e) {
                btn.setOpaque(false);
                btn.setBackground(pnlMenu.getBackground()); // Hoặc một màu cụ thể
                btn.setForeground(Color.BLACK); // Đặt lại màu chữ về mặc định
            }
        });
    }


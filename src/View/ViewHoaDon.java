    private void txtTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtTimKiemActionPerformed
        // TODO add your handling code here:
        String s = txtTimKiem.getText().trim().toString();
        ArrayList<ModelHoaDon> list = new ArrayList<>();
        list.addAll(hoaDon.oTimKiem(s));

        if (s.isBlank()) {
            JOptionPane.showMessageDialog(jPanel1, "Thông tin tìm kiếm trống", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
        } else if (list == null || list.isEmpty()) {
            mol.setRowCount(0);
            JOptionPane.showMessageDialog(jPanel1, "Không có kết quả nào phù hợp", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            filltable(list);
            lblSLuongHoaDon.setText(String.valueOf(tblHoaDon.getRowCount()));
        }
    }//GEN-LAST:event_txtTimKiemActionPerformed

    private void thoiGianBatDauOnSelectionChange(datechooser.events.SelectionChangedEvent evt) {//GEN-FIRST:event_thoiGianBatDauOnSelectionChange
        // TODO add your handling code here:
        if (thoiGianBatDau.getSelectedDate() == null) {
            dateDialog.setVisible(false);
        } else if (checkThoiGianTimKiem == 0) {
            Date selectedDate = thoiGianBatDau.getSelectedDate().getTime();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            txtThoiGianBatDau.setText(sdf.format(selectedDate));
        } else if (checkThoiGianTimKiem == 1) {
            Date selectedDate = thoiGianBatDau.getSelectedDate().getTime();
            SimpleDateFormat sdf = new SimpleDateFormat("yyy-MM-dd");
            txtThoiGianKetThuc.setText(sdf.format(selectedDate));
        }
    }//GEN-LAST:event_thoiGianBatDauOnSelectionChange

    private void btnXemChiTietActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXemChiTietActionPerformed
        // TODO add your handling code here:
        
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn hóa đơn nào!");
            return;
        }

        String maHoaDon = tblHoaDon.getValueAt(index, 0).toString();
        ModelHoaDonInHoaDon hd = hoaDon.getChiTietHoaDon(maHoaDon);

        if (hd == null) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy thông tin hóa đơn!");
            return;
        }

        try {
            txt_MaHoaDon.setText(hd.getMaHoaDon() != null ? hd.getMaHoaDon() : "");
            txt_NgayMuaHang.setText(hd.getNgayMuaHang() != null ? hd.getNgayMuaHang() : "");
            txt_TongTien.setText(String.valueOf(hd.getTongTien()));
            txt_GiaGiam.setText(String.valueOf(hd.getGiaGiam()));
            txt_GiaSauGiam.setText(String.valueOf(hd.getGiaSauGiam()));
            txt_TrangThai.setText(hd.getTrangThai() != null ? (hd.getTrangThai() ? "Đã thanh toán" : "Chưa thanh toán") : "");
            txt_TenNhanVien.setText(hd.getTenNhanVien() != null ? hd.getTenNhanVien() : "");
            txt_MaNhanVien.setText(hd.getMaNhanVien() != null ? hd.getMaNhanVien() : "");
            txt_TenKhachHang.setText(hd.getTenKhachHang() != null ? hd.getTenKhachHang() : "");
            txt_SoDienThoai.setText(hd.getSdtKhachHang() != null ? hd.getSdtKhachHang() : "");
            txt_MaKhuyenMai.setText(hd.getMaKhuyenMai() != null ? hd.getMaKhuyenMai() : "");
            txt_TenKhuyenMai.setText(hd.getTenKhuyenMai() != null ? hd.getTenKhuyenMai() : "");

            hoaDonChiTiet.setSize(400, 600);
            hoaDonChiTiet.setLocationRelativeTo(this);
            hoaDonChiTiet.setVisible(true);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi hiển thị thông tin hóa đơn!");
        }
    }//GEN-LAST:event_btnXemChiTietActionPerformed

    private void btnInHoaDonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInHoaDonActionPerformed
        // TODO add your handling code here:
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn hóa đơn nào!");
            return;
        }

        String maHoaDon = tblHoaDon.getValueAt(index, 0).toString();
        ModelHoaDonInHoaDon hd = hoaDon.getChiTietHoaDon(maHoaDon);
        ArrayList<ModelHoaDonChiTiet> listHdct = hoaDon.danhSachHoaDonChiTiet(maHoaDon);

        if (hd == null || listHdct == null) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy thông tin hóa đơn!");
            return;
        }

        try {
            InHoaDon1.taoHoaDon(hd, listHdct);
            JOptionPane.showMessageDialog(this, "Hóa đơn đã được in thành công!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi in hóa đơn: " + e.getMessage());
        }
    }//GEN-LAST:event_btnInHoaDonActionPerformed


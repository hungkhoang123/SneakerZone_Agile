    private ModelHoaDon readform() {
        Boolean trangThai = null;
            Integer donGiaA;
        Integer donGiaB;
        String ngayBatDau = txtThoiGianBatDau.getText().trim();
        String ngayKetThuc = txtThoiGianKetThuc.getText().trim();
        if (ngayBatDau.isBlank()) {
            ngayBatDau = null;
        }

        if (ngayKetThuc.isBlank()) {
            ngayKetThuc = null;
        }

        if (cboTrangThai.getSelectedItem().equals("Đã thanh toán")) {
            trangThai = true;
        } else if (cboTrangThai.getSelectedItem().equals("Chưa thanh toán")) {
            trangThai = false;
        }

        if (cboDonGia.getSelectedItem().equals("Dưới 1 triệu")) {
            donGiaA = 0;
            donGiaB = 1000000;
        } else if (cboDonGia.getSelectedItem().equals("1 đến 5triệu")) {
            donGiaA = 1000000;
            donGiaB = 5000000;
        } else if (cboDonGia.getSelectedItem().equals("5 đến 10 triệu")) {
            donGiaA = 5000000;
            donGiaB = 10000000;
        } else if (cboDonGia.getSelectedItem().equals("trên 10 triệu")) {
            donGiaA = 10000000;
            donGiaB = 999999999;
        } else {
            donGiaA = null;
            donGiaB = null;
        }
        return new ModelHoaDon(ngayBatDau, donGiaA, trangThai, ngayKetThuc, donGiaB);
    }

